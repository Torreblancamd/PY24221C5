

#OPERADORES RELACIONALES
#VALORES BOOLEANOS: False True

# IGUALDAD ==

x = 30
y = 50
print( 30 == 40 )#False
print( 50 == 50)#True
print( 10 == "Juan" )#False

print( x == 30 )#True
print( y == x + 10 )#False
print("Carlos" == "Carlos" )#True

#Case Sensitive
print("Juan" == "jUaN")#False 
print(" Ramon" == "Ramon")#False




# MAYOR >

print( 15 > 14 )#True
print( 20 + 30 > 60 - 10)#False
print( 100 > 200 )#False
print( "A" > "a" )#OJO 



#MENOR < 

print( 20 < 30 )#True
print( 60 + 20 < 80 - 15 )#False


#MAYOR O IGUAL >=

print( 50 >= 50 )#True
print( 70 >= 80 )#False


#MENOR O IGUAL <=

print( 40 <= 41 )#True
print(30+10<=50-10)#True




# DISTINTO, NO IGUAL != 

print( 10 != 20 )#True
print( 30 != 30 )#False
